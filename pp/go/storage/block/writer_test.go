const (
	numberOfShards uint16 = 1

	maxSegmentSize uint32 = 1024

	unloadDataStorageInterval time.Duration = time.Second

	blockDuration = 2 * time.Hour
)

type WriterSuite struct {
	suite.Suite
	dataDir     string
	head        *storage.Head
	blockWriter *block.Writer[*shard.Shard]
}

func TestWriterSuite(t *testing.T) {
	suite.Run(t, new(WriterSuite))
}

func (s *WriterSuite) SetupTest() {
	s.dataDir = s.createDataDirectory()
	s.head = s.mustCreateHead()
	s.blockWriter = block.NewWriter[*shard.Shard](
		s.dataDir,
		block.DefaultChunkSegmentSize,
		blockDuration,
		prometheus.DefaultRegisterer,
	)
}

func (s *WriterSuite) createDataDirectory() string {
	dataDir := filepath.Join(s.T().TempDir(), "data")
	s.Require().NoError(os.MkdirAll(dataDir, os.ModeDir))
	return dataDir
}

func (s *WriterSuite) mustCreateCatalog() *catalog.Catalog {
	l, err := catalog.NewFileLogV2(filepath.Join(s.dataDir, "catalog.log"))
	s.Require().NoError(err)

	c, err := catalog.New(
		clockwork.NewFakeClock(),
		l,
		&catalog.DefaultIDGenerator{},
		catalog.DefaultMaxLogFileSize,
		nil,
	)
	s.Require().NoError(err)

	return c
}

func (s *WriterSuite) mustCreateHead() *storage.Head {
	h, err := storage.NewBuilder(
		s.mustCreateCatalog(),
		s.dataDir,
		maxSegmentSize,
		prometheus.DefaultRegisterer,
		unloadDataStorageInterval,
	).Build(0, numberOfShards)
	s.Require().NoError(err)
	return h
}

func (s *WriterSuite) mustReadFile(filename string) []byte {
	b, err := os.ReadFile(filename)
	s.Require().NoError(err)
	return b
}

func (s *WriterSuite) mustReadBlockMeta(filename string) tsdb.BlockMeta {
	var meta tsdb.BlockMeta
	s.NoError(json.Unmarshal(s.mustReadFile(filename), &meta))
	return meta
}

func (s *WriterSuite) shard() *shard.Shard {
	for sd := range s.head.RangeShards() {
		return sd
	}

	return nil
}

func (s *WriterSuite) fillHead() {
	ts := time.UnixMilli(1753805651969)
	storagetest.MustAppendTimeSeries(s.T().Context(), s.Require().NoError, s.head, []storagetest.TimeSeries{
		{
			Labels: labels.FromStrings("__name__", "value1"),
			Samples: []cppbridge.Sample{
				{Timestamp: ts.UnixMilli(), Value: 0},
				{Timestamp: ts.Add(time.Minute).UnixMilli(), Value: 1},
				{Timestamp: ts.Add(time.Hour * 2).UnixMilli(), Value: 2},
			},
		},
		{
			Labels: labels.FromStrings("__name__", "value2"),
			Samples: []cppbridge.Sample{
				{Timestamp: ts.UnixMilli(), Value: 0},
				{Timestamp: ts.Add(time.Minute).UnixMilli(), Value: 1},
				{Timestamp: ts.Add(time.Hour * 2).UnixMilli(), Value: 2},
			},
		},
	})
}

