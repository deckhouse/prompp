func (s *WriterSuite) TestWrite() {
	// Arrange
	s.fillHead()

	// Act
	block.LsIdBatchSize = 2
	blocks, err := s.blockWriter.Write(s.shard())

	// Assert
	s.assertWrittenBlocks(blocks, err)
}

